package eznews;

public class IpSec {
	private int ipsecsn;
	private String ipsecstart;
	private String ipsecend;
	public int getIpsecsn() {
		return ipsecsn;
	}
	public void setIpsecsn(int ipsecsn) {
		this.ipsecsn = ipsecsn;
	}
	public String getIpsecstart() {
		return ipsecstart;
	}
	public void setIpsecstart(String ipsecstart) {
		this.ipsecstart = ipsecstart;
	}
	public String getIpsecend() {
		return ipsecend;
	}
	public void setIpsecend(String ipsecend) {
		this.ipsecend = ipsecend;
	}
}
